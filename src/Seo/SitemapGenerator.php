    public function addRoutesFromRouter(Router $router, float $defaultPriority = 0.5): self
    {
        $routes = $router->getRoutes();

        foreach ($routes as $route) {
            // Seulement les routes GET
            $methods = $route['methods'] ?? ['GET'];
            if (!in_array('GET', $methods)) {
                continue;
            }

            $path = $route['path'] ?? '';
            $name = $route['name'] ?? '';

            // Ignorer les routes avec paramÃ¨tres dynamiques (ex: /user/{id})
            if (preg_match('/\{[^}]+\}/', $path)) {
                continue;
            }

            // VÃ©rifier les patterns d'exclusion
            if ($this->isExcluded($path)) {
                continue;
            }

            // DÃ©terminer la prioritÃ©
            $priority = $defaultPriority;
            if ($path === '/' || $name === 'index' || $name === 'home') {
                $priority = 1.0;
            }

            $this->addUrl($path, null, 'weekly', $priority);
        }

        return $this;
    }

